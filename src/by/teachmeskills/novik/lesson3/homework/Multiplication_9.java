package by.teachmeskills.novik.lesson3.homework;

public class Multiplication_9 {
    public static void main(String[] args) {
        System.out.println("Таблица умножения:");

        for (int i = 1; i <= 10; ++i) {
            for (int j = 1; j <= 10; ++j) {
                System.out.print(i + "*" + j + "=" + (i * j) + " ");

            }
            System.out.println();}

    }
}




